public class PartBTask7 {
    public static void main(String[] args) {
        // Nested for loop to create triangle of asterisks
        for (int row = 1; row <= 5; row++) {
            for (int col = 1; col <= 5; col++) {
                System.out.print("* ");
            }
            System.out.println();
        }
    }
}